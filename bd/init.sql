CREATE OR REPLACE FUNCTION verifier_age_prestataire()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF age(CURRENT_DATE, NEW.date_naissance) < interval '18 years' THEN
        RAISE EXCEPTION 'Prestataire doit avoir au moins 18 ans';
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION verifier_age_client()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF age(CURRENT_DATE, NEW.date_naissance) < interval '16 years' THEN
        RAISE EXCEPTION 'Client doit avoir au moins 16 ans';
    END IF;

    RETURN NEW;
END;
$$;

