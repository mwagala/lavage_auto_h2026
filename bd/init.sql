DROP SCHEMA IF EXISTS public CASCADE;
CREATE SCHEMA public;
SET search_path TO public;

CREATE TABLE Prestataires (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nom varchar(100) NOT NULL,
    prenoms varchar(100) NOT NULL,
    date_naissance date NOT NULL,
    nas varchar(100) NOT NULL UNIQUE,
    note_moyenne decimal(3,2) NOT NULL DEFAULT 0 CHECK (note_moyenne BETWEEN 0 AND 5),
    courriel varchar(255) NOT NULL UNIQUE,
    telephone varchar(50) NOT NULL UNIQUE,
    adresse_numero varchar(10) NOT NULL,
    adresse_rue varchar(150) NOT NULL,
    adresse_ville varchar(100) NOT NULL,
    adresse_province varchar(100) NOT NULL,
    adresse_code_postal varchar(20) NOT NULL,
    mot_passe_hash varchar(255) NOT NULL,
    est_actif boolean NOT NULL DEFAULT TRUE,
    cree_a timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    modifier_a timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Clients (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nom varchar(100) NOT NULL,
    prenoms varchar(100) NOT NULL,
    date_naissance date NOT NULL,
    courriel varchar(255) NOT NULL UNIQUE,
    telephone varchar(50) NOT NULL UNIQUE,
    mode_paiement varchar(100) NOT NULL,
    adresse_numero varchar(10) NOT NULL,
    adresse_rue varchar(150) NOT NULL,
    adresse_ville varchar(100) NOT NULL,
    adresse_province varchar(100) NOT NULL,
    adresse_code_postal varchar(20) NOT NULL,
    mot_passe_hash varchar(255) NOT NULL,
    est_actif boolean NOT NULL DEFAULT TRUE,
    cree_a timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    modifier_a timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Disponibilites (
    prestataire_id integer NOT NULL,
    jour varchar(100) NOT NULL,
    statut varchar(190) NOT NULL,
    heure_debut time NOT NULL,
    heure_fin time NOT NULL,
    CONSTRAINT pk_dispo_prestataire PRIMARY KEY (prestataire_id, jour, heure_debut, heure_fin),
    CONSTRAINT fk_dispo_prestataire_id FOREIGN KEY (prestataire_id)
        REFERENCES Prestataires(id) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT ck_dispo_heure CHECK (heure_debut < heure_fin)
);

CREATE TABLE Services (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nom varchar(255) NOT NULL,
    prestataire_id integer NOT NULL,
    description text NOT NULL,
    duree decimal(5,2) NOT NULL CHECK (duree > 0),
    prix decimal(10,2) NOT NULL CHECK (prix > 0),
    CONSTRAINT fk_service_prestataire_id FOREIGN KEY (prestataire_id)
        REFERENCES Prestataires(id) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT uq_service_nom_prestataire UNIQUE (prestataire_id, nom)
);

CREATE TABLE Reservations (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    client_id integer NOT NULL,
    prestataire_id integer NOT NULL,
    heure_debut time NOT NULL,
    heure_fin time,
    date date NOT NULL,
    statut varchar(30) NOT NULL DEFAULT 'Assignee',
    CONSTRAINT ck_reservation_statut CHECK (statut IN ('Assignee', 'En cours', 'annulee', 'Terminee')),
    CONSTRAINT fk_reservation_client_id FOREIGN KEY (client_id)
        REFERENCES Clients(id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_reservation_prestataire_id FOREIGN KEY (prestataire_id)
        REFERENCES Prestataires(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE Reservation_Services (
    reservation_id integer NOT NULL,
    service_id integer NOT NULL,
    prix_applique decimal(10,2) NOT NULL CHECK (prix_applique > 0),
    duree_prevue decimal(5,2) NOT NULL CHECK (duree_prevue > 0),
    quantite integer NOT NULL DEFAULT 1 CHECK (quantite >= 1),
    PRIMARY KEY (reservation_id, service_id),
    CONSTRAINT fk_rs_reservation_id FOREIGN KEY (reservation_id)
        REFERENCES Reservations(id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_rs_service_id FOREIGN KEY (service_id)
        REFERENCES Services(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE Commentaires (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    client_id integer NOT NULL,
    reservation_id integer UNIQUE NOT NULL,
    texte text NOT NULL,
    note integer NOT NULL CHECK (note BETWEEN 1 AND 5),
    CONSTRAINT fk_comment_client_id FOREIGN KEY (client_id)
        REFERENCES Clients(id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_comment_reservation_id FOREIGN KEY (reservation_id)
        REFERENCES Reservations(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE Factures (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    reservation_id integer NOT NULL UNIQUE,
    sous_total decimal(10,2) NOT NULL CHECK (sous_total >= 0),
    total decimal(10,2) NOT NULL CHECK (total >= 0),
    CONSTRAINT fk_facture_reservation_id FOREIGN KEY (reservation_id)
        REFERENCES Reservations(id) ON UPDATE CASCADE ON DELETE CASCADE
);

