CREATE OR REPLACE FUNCTION recalculer_note_moyenne_prestataire(p_prestataire_id integer)
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
    UPDATE Prestataires p
    SET note_moyenne = COALESCE((
        SELECT ROUND(AVG(c.note), 2)
        FROM Commentaires c
        INNER JOIN Reservations r
            ON r.id = c.reservation_id
        WHERE r.prestataire_id = p_prestataire_id
    ), 0)
    WHERE p.id = p_prestataire_id;
END;
$$;

CREATE OR REPLACE FUNCTION recalculer_note_moyenne_commentaire()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    v_prestataire_id integer;
BEGIN
    SELECT r.prestataire_id
    INTO v_prestataire_id
    FROM Reservations r
    WHERE r.id = CASE
        WHEN TG_OP = 'DELETE' THEN OLD.reservation_id
        ELSE NEW.reservation_id
    END;

    PERFORM recalculer_note_moyenne_prestataire(v_prestataire_id);

    IF TG_OP = 'DELETE' THEN
        RETURN OLD;
    END IF;

    RETURN NEW;
END;
$$;

CREATE TRIGGER commentaires_note_moyenne_aiud
AFTER INSERT OR UPDATE OR DELETE ON Commentaires
FOR EACH ROW
EXECUTE FUNCTION recalculer_note_moyenne_commentaire();

CREATE OR REPLACE FUNCTION verifier_limite_reservations_client(
    p_reservation_id integer,
    p_client_id integer,
    p_date date
)
RETURNS void
LANGUAGE plpgsql
AS $$
DECLARE
    v_nb integer;
BEGIN
    SELECT COUNT(*)
    INTO v_nb
    FROM Reservations r
    WHERE r.client_id = p_client_id
      AND r.date = p_date
      AND r.statut <> 'annulee'
      AND (p_reservation_id IS NULL OR r.id <> p_reservation_id);

    IF v_nb >= 2 THEN
        RAISE EXCEPTION 'Limite atteinte : un client ne peut pas avoir plus de 2 reservations le meme jour.';
    END IF;
END;
$$;

CREATE OR REPLACE FUNCTION verifier_limite_reservations_client_trigger()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.statut <> 'annulee' THEN
        PERFORM verifier_limite_reservations_client(
            CASE WHEN TG_OP = 'INSERT' THEN NULL ELSE NEW.id END,
            NEW.client_id,
            NEW.date
        );
    END IF;

    RETURN NEW;
END;
$$;

CREATE TRIGGER verifier_limite_reservations_client_bi
BEFORE INSERT ON Reservations
FOR EACH ROW
EXECUTE FUNCTION verifier_limite_reservations_client_trigger();

CREATE TRIGGER verifier_limite_reservations_client_bu
BEFORE UPDATE ON Reservations
FOR EACH ROW
WHEN (OLD.client_id IS DISTINCT FROM NEW.client_id OR OLD.date IS DISTINCT FROM NEW.date OR OLD.statut IS DISTINCT FROM NEW.statut)
EXECUTE FUNCTION verifier_limite_reservations_client_trigger();

CREATE OR REPLACE FUNCTION verifier_chevauchement_reservation(
    p_reservation_id integer,
    p_prestataire_id integer,
    p_date date,
    p_heure_debut time,
    p_heure_fin time
)
RETURNS void
LANGUAGE plpgsql
AS $$
DECLARE
    v_nb integer;
BEGIN
    SELECT COUNT(*)
    INTO v_nb
    FROM Reservations r
    WHERE r.prestataire_id = p_prestataire_id
      AND r.date = p_date
      AND r.statut IN ('Assignee', 'En cours')
      AND r.id <> p_reservation_id
      AND r.heure_fin IS NOT NULL
      AND p_heure_debut < r.heure_fin
      AND p_heure_fin > r.heure_debut;

    IF v_nb > 0 THEN
        RAISE EXCEPTION 'Conflit: une autre reservation existe deja sur cette plage horaire pour ce prestataire.';
    END IF;
END;
$$;

CREATE OR REPLACE FUNCTION recalculer_heure_fin_reservation(p_reservation_id integer)
RETURNS void
LANGUAGE plpgsql
AS $$
DECLARE
    v_heure_debut time;
    v_heure_fin time;
    v_duree_totale decimal(10,2);
    v_prestataire_id integer;
    v_date date;
    v_statut varchar(30);
BEGIN
    SELECT r.heure_debut, r.prestataire_id, r.date, r.statut
    INTO v_heure_debut, v_prestataire_id, v_date, v_statut
    FROM Reservations r
    WHERE r.id = p_reservation_id;

    IF NOT FOUND THEN
        RETURN;
    END IF;

    SELECT COALESCE(SUM(rs.duree_prevue * rs.quantite), 0)
    INTO v_duree_totale
    FROM Reservation_Services rs
    WHERE rs.reservation_id = p_reservation_id;

    IF v_duree_totale = 0 THEN
        v_heure_fin = v_heure_debut;
    ELSE
        v_heure_fin = v_heure_debut + (v_duree_totale::double precision * interval '1 hour');
    END IF;

    IF v_statut <> 'annulee' THEN
        PERFORM verifier_chevauchement_reservation(
            p_reservation_id,
            v_prestataire_id,
            v_date,
            v_heure_debut,
            v_heure_fin
        );
    END IF;

    UPDATE Reservations
    SET heure_fin = v_heure_fin
    WHERE id = p_reservation_id;
END;
$$;

CREATE OR REPLACE FUNCTION recalculer_facture_reservation(p_reservation_id integer)
RETURNS void
LANGUAGE plpgsql
AS $$
DECLARE
    v_sous_total decimal(10,2);
BEGIN
    SELECT COALESCE(SUM(rs.prix_applique * rs.quantite), 0)
    INTO v_sous_total
    FROM Reservation_Services rs
    WHERE rs.reservation_id = p_reservation_id;

    IF v_sous_total = 0 THEN
        DELETE FROM Factures
        WHERE reservation_id = p_reservation_id;
    ELSE
        INSERT INTO Factures(reservation_id, sous_total, total)
        VALUES (p_reservation_id, v_sous_total, calculer_total(v_sous_total))
        ON CONFLICT (reservation_id)
        DO UPDATE SET
            sous_total = EXCLUDED.sous_total,
            total = EXCLUDED.total;
    END IF;
END;
$$;

CREATE OR REPLACE FUNCTION reservation_services_apres_changement()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF TG_OP = 'DELETE' THEN
        PERFORM recalculer_heure_fin_reservation(OLD.reservation_id);
        PERFORM recalculer_facture_reservation(OLD.reservation_id);
        RETURN OLD;
    END IF;

    IF TG_OP = 'UPDATE' AND OLD.reservation_id <> NEW.reservation_id THEN
        PERFORM recalculer_heure_fin_reservation(OLD.reservation_id);
        PERFORM recalculer_facture_reservation(OLD.reservation_id);
    END IF;

    PERFORM recalculer_heure_fin_reservation(NEW.reservation_id);
    PERFORM recalculer_facture_reservation(NEW.reservation_id);
    RETURN NEW;
END;
$$;

CREATE TRIGGER reservation_services_apres_insert
AFTER INSERT ON Reservation_Services
FOR EACH ROW
EXECUTE FUNCTION reservation_services_apres_changement();

CREATE TRIGGER reservation_services_apres_update
AFTER UPDATE ON Reservation_Services
FOR EACH ROW
EXECUTE FUNCTION reservation_services_apres_changement();

CREATE TRIGGER reservation_services_apres_delete
AFTER DELETE ON Reservation_Services
FOR EACH ROW
EXECUTE FUNCTION reservation_services_apres_changement();
