CREATE OR REPLACE FUNCTION calculer_total(sous_total decimal(10,2))
RETURNS decimal(10,2)
LANGUAGE plpgsql
IMMUTABLE
AS $$
BEGIN
    RETURN ROUND(sous_total * 1.14975, 2);
END;
$$;

